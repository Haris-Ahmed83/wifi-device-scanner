def lookup_vendor(mac_address: str) -> str:
    mac_clean = mac_address.replace("-", ":").replace(".", "").lower()
    parts = mac_clean.split(":")
    if len(parts) < 3:
        return "Unknown"

    oui = f"{parts[0]}:{parts[1]}:{parts[2]}".upper()
    oui_variants = [
        oui,
        f"{parts[0]}:{parts[1]}:{parts[2]}".lower(),
    ]

    lookup_oui = oui.upper()
    if lookup_oui in OUI_DATABASE:
        return OUI_DATABASE[lookup_oui]

    oui_len = f"{parts[0]}:{parts[1]}".upper()
    for oui_prefix, vendor in OUI_DATABASE.items():
        if oui_prefix.startswith(oui_len):
            return vendor

    return "Unknown"
